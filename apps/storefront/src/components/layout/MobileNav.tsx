export default function MobileNav() {
  const pathname = usePathname()
  const { totalItems } = useCart()

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 border-t border-brand-100 bg-white md:hidden">
      <div className="flex items-center justify-around py-2.5">
        {navItems.map((item) => {
          const isActive = pathname === item.href || (item.href !== '/' && pathname.startsWith(item.href))
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`relative flex flex-col items-center gap-1 px-4 py-0.5 transition-colors ${
                isActive ? 'text-brand-900' : 'text-brand-300'
              }`}
            >
              {item.icon}
              {item.showBadge && totalItems > 0 && (
                <span className="absolute right-1 top-0 flex h-3.5 w-3.5 items-center justify-center bg-brand-900 text-[8px] font-bold text-white">
                  {totalItems}
                </span>
              )}
              <span className="text-[9px] font-semibold uppercase tracking-wider">{item.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
